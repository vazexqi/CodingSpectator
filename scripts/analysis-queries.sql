AVG(
  CASE
  WHEN "PUBLIC"."ALL_DATA"."navigation duration" = '' THEN
    NULL
  ELSE
    CAST("PUBLIC"."ALL_DATA"."navigation duration" AS BIGINT)
  END
) AS "AVG_CONFIGURATION_DURATION_IN_MILLI_SEC"
